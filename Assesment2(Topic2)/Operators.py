# operators
# Arithmetic Operators
print("5 + 3 =", 5 + 3)
print("10 // 3 =", 10 // 3)
print("2 ** 4 =", 2 ** 4)
# Comparison Operators
print("5 > 3:", 5 > 3)
print("5 == 5:", 5 == 5)
# Logical Operators
print("True and False =", True and False)
print("not True =", not True)
# Assignment Operators
x = 10
x += 5
print("x after += 5:", x)
